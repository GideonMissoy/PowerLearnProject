void main() {
  // Int data type
  int age = 25;
  print('Age: $age');

  // Double data type
  double height = 5.9;
  print('Height: $height feet');

  // String data type
  String name = 'John Doe';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isStudent': false,
  };
  print('Person: $person');
  }
